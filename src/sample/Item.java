package sample;

/*
 * interface item sets up skeleton methods.
 * @author: Nicholis Wright
 * */
interface Item {

  /*
   * Skeleton for getID method
   * @author: Nicholis Wright
   * */
  int getId();

  /*
   * Skeleton method for setName method
   * @author: Nicholis Wright
   * */
  void setName(String name);

  /*
   * Skeleton for getName method.
   * @author: Nicholis Wright
   * */
  String getName();

  /*
   * Skeleton method for setManufacturer.
   * @author: Nicholis Wright
   * */
  void setManufacturer(String manufacturer);

  /*
   * Skeleton method for getManufacturer.
   * @author: Nicholis Wright
   * */
  String getManufacturer();
}
